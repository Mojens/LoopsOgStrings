package WhileLoop;

public class ForFun {
  public static void main(String[] args) {
    for (int i=1; i <= 10; i++){ //i er 0 og så længe i<10 så skal den i++ = i=i+1
      // først del er [Datatype] "variabel navn" = "start værdi;
      // anden del efter ; er "variabel navn" skal være mindre end eller lig med "maximum antal du ønsker loopet skal køres;
      // trejde del er "variabel navn" stiger med 1
      System.out.println(i);
    }
  }
}

